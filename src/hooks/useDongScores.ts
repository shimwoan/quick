import { useEffect, useState } from 'react';
import { supabase } from '../lib/supabase';

export interface DongHeatData {
  dong_code: string;
  dong_name: string;
  gu_name: string;
  call_expectation: number;
  lat: number;
  lng: number;
}

export function useDongScores() {
  const [dongs, setDongs] = useState<DongHeatData[]>([]);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    async function fetch() {
      const { data: boundaries } = await supabase
        .from('dong_boundaries')
        .select('dong_code, dong_name, gu_name');

      const { data: scores } = await supabase
        .from('dong_scores')
        .select('dong_code, call_expectation');

      if (!boundaries || !scores) {
        setLoading(false);
        return;
      }

      const scoreMap = new Map<string, number>();
      for (const s of scores) {
        scoreMap.set(s.dong_code, s.call_expectation);
      }

      // 시드 데이터의 center_point 좌표 (PostGIS에서 직접 못 가져오므로 하드코딩)
      const centers = getCenterMap();

      const result: DongHeatData[] = [];
      for (const b of boundaries) {
        const score = scoreMap.get(b.dong_code) ?? 0;
        const center = centers.get(b.dong_code);
        if (!center) continue;

        result.push({
          dong_code: b.dong_code,
          dong_name: b.dong_name,
          gu_name: b.gu_name,
          call_expectation: score,
          lat: center.lat,
          lng: center.lng,
        });
      }

      setDongs(result);
      setLoading(false);
    }

    fetch();
  }, []);

  return { dongs, loading };
}

