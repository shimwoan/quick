async function main() {
  console.log("=== Seoul Dong Seed Data ===\n");
  console.log(`Seeding ${DONG_DATA.length} administrative districts...\n`);

  // ── Step 1: Clear existing data (order matters for FK constraints) ────
  console.log("[1/5] Clearing existing data...");
  await supabase.from("dong_adjacency").delete().neq("dong_code_1", "");
  await supabase.from("dong_scores").delete().neq("dong_code", "");
  await supabase.from("dong_boundaries").delete().neq("dong_code", "");
  console.log("  Done.\n");

  // ── Step 2: Insert dong_boundaries ────────────────────────────────────
  console.log("[2/5] Inserting dong_boundaries...");
  const boundaryRows = DONG_DATA.map((d) => ({
    dong_code: d.dong_code,
    dong_name: d.dong_name,
    gu_name: d.gu_name,
    boundary: makePlaceholderMultiPolygon(d.lat, d.lng),
    center_point: makePoint(d.lat, d.lng),
    area_km2: d.area_km2,
  }));

  const { error: boundaryError } = await supabase
    .from("dong_boundaries")
    .insert(boundaryRows);

  if (boundaryError) {
    console.error("  Error inserting dong_boundaries:", boundaryError.message);
    process.exit(1);
  }
  console.log(`  Inserted ${boundaryRows.length} dong_boundaries.\n`);

  // ── Step 3: Insert dong_scores ────────────────────────────────────────
  console.log("[3/5] Inserting dong_scores...");
  const scoreRows = DONG_DATA.map((d) => ({
    dong_code: d.dong_code,
    logistics_count: d.logistics_count,
    office_count: d.office_count,
    hospital_count: d.hospital_count,
    shopping_count: d.shopping_count,
    general_store_count: d.general_store_count,
    legal_count: d.legal_count,
    wholesale_market_count: d.wholesale_market_count,
    call_expectation: 0,
  }));

  const { error: scoreError } = await supabase
    .from("dong_scores")
    .insert(scoreRows);

  if (scoreError) {
    console.error("  Error inserting dong_scores:", scoreError.message);
    process.exit(1);
  }
  console.log(`  Inserted ${scoreRows.length} dong_scores.\n`);

  // ── Step 4: Calculate call_expectation via RPC ────────────────────────
  console.log("[4/5] Calculating call_expectation scores (RPC)...");
  const { error: rpcError } = await supabase.rpc("calculate_dong_scores");

  if (rpcError) {
    console.error("  Error calling calculate_dong_scores():", rpcError.message);
    console.error(
      "  Continuing — you can run the function manually in SQL later."
    );
  } else {
    console.log("  call_expectation scores calculated.\n");
  }

  // ── Step 5: Generate dong_adjacency (center-to-center < 2km) ─────────
  console.log("[5/5] Generating dong_adjacency (distance < 2km)...");
  const adjacencyRows: {
    dong_code_1: string;
    dong_code_2: string;
    distance_km: number;
  }[] = [];

  for (let i = 0; i < DONG_DATA.length; i++) {
    for (let j = i + 1; j < DONG_DATA.length; j++) {
      const a = DONG_DATA[i];
      const b = DONG_DATA[j];
      const dist = haversineKm(a.lat, a.lng, b.lat, b.lng);
      if (dist < 2.0) {
        adjacencyRows.push({
          dong_code_1: a.dong_code,
          dong_code_2: b.dong_code,
          distance_km: Math.round(dist * 1000) / 1000,
        });
        adjacencyRows.push({
          dong_code_1: b.dong_code,
          dong_code_2: a.dong_code,
          distance_km: Math.round(dist * 1000) / 1000,
        });
      }
    }
  }

  if (adjacencyRows.length > 0) {
    // Insert in batches of 500 to avoid payload limits
    const batchSize = 500;
    for (let i = 0; i < adjacencyRows.length; i += batchSize) {
      const batch = adjacencyRows.slice(i, i + batchSize);
      const { error: adjError } = await supabase
        .from("dong_adjacency")
        .insert(batch);

      if (adjError) {
        console.error("  Error inserting dong_adjacency:", adjError.message);
        process.exit(1);
      }
    }
  }
  console.log(
    `  Inserted ${adjacencyRows.length} adjacency pairs (${adjacencyRows.length / 2} unique pairs).\n`
  );

  // ── Summary ───────────────────────────────────────────────────────────
  console.log("=== Summary ===");
  console.log(`  dong_boundaries : ${DONG_DATA.length} rows`);
  console.log(`  dong_scores     : ${DONG_DATA.length} rows`);
  console.log(
    `  dong_adjacency  : ${adjacencyRows.length} rows (${adjacencyRows.length / 2} pairs)`
  );

  // Print top-scoring dongs
  const { data: topScores } = await supabase
    .from("dong_scores")
    .select("dong_code, call_expectation")
    .order("call_expectation", { ascending: false })
    .limit(10);

  if (topScores && topScores.length > 0) {
    console.log("\n  Top 10 call_expectation scores:");
    for (const row of topScores) {
      const dong = DONG_DATA.find((d) => d.dong_code === row.dong_code);
      const name = dong
        ? `${dong.gu_name} ${dong.dong_name}`
        : row.dong_code;
      console.log(
        `    ${name.padEnd(25)} ${String(row.call_expectation).padStart(6)}`
      );
    }
  }

  // Print gu-level breakdown
  const guMap = new Map<string, number>();
  for (const d of DONG_DATA) {
    guMap.set(d.gu_name, (guMap.get(d.gu_name) || 0) + 1);
  }
  console.log("\n  Districts per 구:");
  for (const [gu, count] of Array.from(guMap.entries()).sort()) {
    console.log(`    ${gu.padEnd(10)} ${count}개 동`);
  }

  console.log("\nSeed complete.");
}

main().catch((err) => {
  console.error("Fatal error:", err);
  process.exit(1);
});
