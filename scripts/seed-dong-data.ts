/**
 * Create a small placeholder MultiPolygon bounding box (~200m) around a point.
 * This satisfies the NOT NULL MultiPolygon constraint on dong_boundaries.boundary.
 * Replace with real polygon data later.
 */
function makePlaceholderMultiPolygon(lat: number, lng: number): string {
  const d = 0.001; // ~111m offset
  return `SRID=4326;MULTIPOLYGON(((${lng - d} ${lat - d}, ${lng + d} ${lat - d}, ${lng + d} ${lat + d}, ${lng - d} ${lat + d}, ${lng - d} ${lat - d})))`;
}

function makePoint(lat: number, lng: number): string {
  return `SRID=4326;POINT(${lng} ${lat})`;
}

/**
 * Haversine distance in km between two lat/lng pairs
 */
function haversineKm(
  lat1: number,
  lng1: number,
  lat2: number,
  lng2: number
): number {
  const R = 6371;
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLng = ((lng2 - lng1) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLng / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

